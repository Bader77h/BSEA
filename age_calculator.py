
from datetime import date

def حساب_العمر():
    try:
        # إدخال تاريخ الميلاد
        سنة_الميلاد = int(input("أدخل سنة الميلاد: "))
        شهر_الميلاد = int(input("أدخل شهر الميلاد (1-12): "))
        يوم_الميلاد = int(input("أدخل يوم الميلاد: "))

        # تحديد التاريخ الحالي
        اليوم = date.today()

        # حساب العمر
        العمر_بالسنوات = اليوم.year - سنة_الميلاد
        العمر_بالأشهر = اليوم.month - شهر_الميلاد
        العمر_بالأيام = اليوم.day - يوم_الميلاد

        # التحقق من تصحيح العمر إذا كان الشهر أو اليوم أقل
        if العمر_بالأشهر < 0 or (العمر_بالأشهر == 0 and العمر_بالأيام < 0):
            العمر_بالسنوات -= 1

        print(f"عمرك الآن: {العمر_بالسنوات} سنة")
    except ValueError:
        print("يرجى إدخال بيانات صحيحة!")

# استدعاء الدالة
حساب_العمر()
